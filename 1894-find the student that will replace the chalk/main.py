class Solution(object):
    def chalkReplacer(self, chalk, k):
        """
        :type chalk: List[int]
        :type k: int
        :rtype: int
        """
        L = len(chalk)
        prefix = [0] * (L + 1)

        for x in range(L):
            prefix[x + 1] = prefix[x] + chalk[x]
        target = k % prefix[L]

        from bisect import bisect_left
        prefix.pop(0)
        idx = bisect_left(prefix, target)

        return idx + 1 if prefix[idx] == target else idx


