class Solution(object):
    def splitArray(self, nums, m):
        """
        :type nums: List[int]
        :type m: int
        :rtype: int
        """
        N = len(nums) + 1
        M = m + 1

        # calculate prefix sum
        prefix_sum = [0] * N
        for x in range(1, N):
            prefix_sum[x] += prefix_sum[x-1] + nums[x-1]

        # dp[x][y] - min sub array sum for x sub arrays for nums[:y]
        dp = [[float("inf")] * N for _ in range(M)]
        for x in range(M):
            for y in range(N):
                if x == 0 and y == 0:
                    dp[x][y] = 0
                    continue

                # 0 sub array must have a min sub array sum for inf
                if x == 0:
                    dp[x][y] = float("inf")
                    continue

                # the min sub array sum for empty array is 0
                if y == 0:
                    dp[x][y] = 0
                    continue

                for z in range(y):
                    dp[x][y] = min(dp[x][y], max(dp[x-1][z], prefix_sum[y] - prefix_sum[z]))
        return dp[M-1][N-1]


