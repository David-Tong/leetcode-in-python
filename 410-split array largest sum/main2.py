class Solution(object):
    def splitArray(self, nums, m):
        """
        :type nums: List[int]
        :type m: int
        :rtype: int
        """
        def canSplitUnderX(nums, m, x):
            count = 0
            sumi = 0
            for num in nums:
                sumi += num
                if sumi > x:
                    count += 1
                    sumi = num
            if sumi > 0:
                count += 1
            if count <= m:
                return True
            else:
                return False

        left = max(nums)
        right = sum(nums)
        while left + 1 < right:
            middle = (left + right) // 2
            if canSplitUnderX(nums, m, middle):
                right = middle
            else:
                left = middle + 1

        if canSplitUnderX(nums, m, left):
            return left
        else:
            return right


