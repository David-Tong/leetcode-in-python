class Solution(object):
    def canDistribute(self, nums, quantity):
        """
        :type nums: List[int]
        :type quantity: List[int]
        :rtype: bool
        """
        # pre-process
        from collections import defaultdict
        dicts = defaultdict(int)
        for num in nums:
            dicts[num] += 1

        digits = list(dicts.values())
        digits = [0] + digits
        # print(digits)
        N = len(digits)
        M = len(quantity)
        L = 2 ** M

        # process
        # dp init
        # dp[x][state] - can distribute repeating integers for customers of combination state with digits[: x]
        dp = [[False] * L for _ in range(N)]
        for state in range(L):
            dp[0][state] = False
        dp[0][0] = True

        # helper function
        # distribute - can digits[x] used to distribute repeating integers for customers of subset
        cache = defaultdict(bool)
        def distribute(digit, subset):
            key = "{}-{}".format(digit, subset)
            if key in cache:
                return cache[key]
            idx = 0
            while subset:
                if subset & 1:
                    digit -= quantity[idx]
                idx += 1
                subset >>= 1
                if digit < 0:
                    cache[key] = False
                    return False
            cache[key] = True
            return True

        # dp transfer
        # case 1 : nums[x] is not used to distribute repeating integers
        #          dp[x][state] = dp[x - 1][state]
        # case 2 : nums[x] is used to distribute repeating integers for customers of subset
        #          dp[x][state] |= dp[x][state - subset] & distribute(digits[x], subset)
        for x in range(1, N):
            for state in range(L):
                dp[x][state] = dp[x - 1][state]
                subset = state
                while subset:
                    dp[x][state] |= dp[x - 1][state - subset] & distribute(digits[x], subset)
                    subset = (subset - 1) & state
        # print(dp)
        ans = dp[N - 1][L - 1]
        return ans


