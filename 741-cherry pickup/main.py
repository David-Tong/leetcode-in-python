class Solution(object):
    def cherryPickup(self, grid):
        """
        :type grid: List[List[int]]
        :rtype: int
        """
        # pre-process
        M = len(grid)
        N = len(grid[0])

        cache = [[[float("-inf")] * M for _ in range(N)] for _ in range(M)]

        # process
        # dp - the maximum number of cherries you can collect when reach grid[x1][y1] and grid[x2][y2]
        #    - x1 + y1 = x2 + y2
        def dp(x1, y1, x2):
            y2 = x1 + y1 - x2
            # print(x1, y1, x2, y2)

            # illegal state
            if x1 < 0 or y1 < 0 or x2 < 0 or y2 < 0:
                return -1
            # searched
            if cache[x1][y1][x2] != float("-inf"):
                return cache[x1][y1][x2]
            # at the starting point
            if x1 == 0 and y1 == 0:
                cache[0][0][0] = grid[0][0]
                return cache[0][0][0]
            # at a thorn
            if grid[x1][y1] == -1 or grid[x2][y2] == -1:
                cache[x1][y1][x2] = -1
                return -1
            # dp transfer
            maxi = max(dp(x1 - 1, y1, x2 - 1),
                                    max(dp(x1 - 1, y1, x2),
                                        max(dp(x1, y1 - 1, x2 - 1),
                                            dp(x1, y1 - 1, x2))))
            if maxi == -1:
                cache[x1][y1][x2] = -1
                return -1
            maxi += grid[x1][y1]
            if x1 != x2:
                maxi += grid[x2][y2]
            cache[x1][y1][x2] = maxi
            return cache[x1][y1][x2]

        return max(0, dp(M - 1, N - 1, M - 1))


